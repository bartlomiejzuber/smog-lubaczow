export const airIndexToEmoji = (airIndex: number) => {
  switch (airIndex) {
    case 1:
      return "😀";
      break;
    case 2:
      return "🙂";
      break;
    case 3:
      return "😐";
      break;
    case 4:
      return "😬";
      break;
    case 5:
      return "😟";
      break;
    case 6:
      return "☹️";
      break;
  }
};
